public class Main {
    public static void main(String[] args) {
      int num=3;
      switch (num)
      {
          case 0:
              System.out.println("num is 0");
              break;

              case 1:
              System.out.println("num is 1");
              break;

              case 3:
              System.out.println("num is 2");
              break;


          default:
              System.out.println(num);
      }
    }
}